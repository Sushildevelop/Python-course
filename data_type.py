# Data Types in Python

# A data type is a classification that tells a programming language:

# what kind of value is stored (number, text, true/false, etc.),

# how much memory it needs,

# what operations are allowed on it.

# example of different data types
integer_var = 42               # Integer
float_var = 3.14               # Float
string_var = "Hello, World!"   # String
boolean_var = True             # Boolean
list_var = [1, 2, 3, 4, 5]     # List
tuple_var = (1, 2, 3)         # Tuple
dict_var = {"key1": "value1", "key2": "value2"}  # Dictionary
set_var = {1, 2, 3}           # Set
