
# Arithmetic operators are used with numeric values to perform common mathematical operations

#       Operator	        Name	                    Example
#       +	                Addition	                x + y	
#       -	                Subtraction	                x - y	
#       *	                Multiplication	            x * y	
#       /	                Division	                x / y	
#       %	                Modulus	                    x % y	
#       **	                Exponentiation	            x ** y	
#       //	                Floor division	            x // y



# Perform operation using Arithmetic Operations
x = 15
y = 4

print("addition operations: ",x + y)
print("Subtraction operations : ",x - y)
print("multiplication operations: ",x * y)
print("division operations: ",x / y)
print("modulus operations: ",x % y)
print("Exponentiation operations : ",x ** y)
print("Floor division: ", x // y)